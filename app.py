# --- Load Data and Models ---
@st.cache_data
def load_data():
    try:
        # Load the data generated by prepare_dashboard_data.py
        with open('artifacts/dashboard_data.json', 'r') as f:
            import json
            data = json.load(f)
            
        top_channels_pd = pd.DataFrame(data['top_10_channels_by_views'])
        months_series_pd = pd.DataFrame(data['monthly_views_series'])
        
        # Load sample data
        df_sample = pd.read_csv('youtube_dislike_dataset.csv', nrows=1000)
        
        # Load predictive model
        model = joblib.load('artifacts/random_forest_model.pkl')
        scaler = joblib.load('artifacts/scaler.pkl')
        feature_names = joblib.load('artifacts/feature_names.pkl')
        
        return top_channels_pd, months_series_pd, df_sample, model, scaler, feature_names
    except FileNotFoundError:
        st.error("Data files not found. Please run `prepare_dashboard_data.py` first!")
        st.stop()

top_channels_df, months_series_df, df, model, scaler, feature_names = load_data()


